// app_wdf_data selection depends on WR_MASK (sys_clk_i domain) and drives ddr3_ip (ui_clk domain) - Needs CDC
assign	app_wdf_data	=	(WR_MASK[0]) ?	mDATAIN1	: mDATAIN2	;

