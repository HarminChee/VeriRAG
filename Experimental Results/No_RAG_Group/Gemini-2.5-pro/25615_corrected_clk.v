  .din					(WR2_DATA),
  .wr_en					(WR2),
  .rd_en					(WR2_RDEN), // Driven by logic clocked by sys_clk_i
  .dout					(mDATAIN2),    // Output used by logic clocked by sys_clk_i
  .full					(WR2_FULL),
  .empty					(WR2_EMPTY),
  .rd_data_count		(write_side_fifo_rusedw2), // Output used by logic clocked by sys_clk_i
