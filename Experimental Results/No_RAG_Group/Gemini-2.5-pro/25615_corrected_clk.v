	wire	[9:0]							write_side_fifo_rusedw2;
	wire	[9:0]							read_side_fifo_wusedw2;


  ddr3_ip #(
   .BANK_WIDTH            	(BANK_WIDTH    ),
   .CK_WIDTH              	(CK_WIDTH      ),
   .CS_WIDTH              	(CS_WIDTH      ),
   .nCS_PER_RANK          	(nCS_PER_RANK  ),
   .CKE_WIDTH             	(CKE_WIDTH     ),
	.DM_WIDTH              	(DM_WIDTH      ),
   .DQ_WIDTH              	(DQ_WIDTH      ),
	.DQS_WIDTH             	(DQS_WIDTH     ),
   .ODT_WIDTH             	(ODT_WIDTH     ),
   .ROW_WIDTH             	(ROW_WIDTH     ),
	.ADDR_WIDTH            	(ADDR_WIDTH    ),
	.PAYLOAD_WIDTH         	(PAYLOAD_WIDTH ),
	.nCK_PER_CLK				(nCK_PER_CLK	),
	.RANKS						(RANKS			)
)	u_ddr3_ip
      (
       .ddr3_addr                      (ddr3_addr),
       .ddr3_ba                        (ddr3_ba),
