	reg rst_sync_r1, rst_sync_r2;
	wire sys_clk_rst; // Active high synchronous reset for logic clocked by sys_clk_i

	always @(posedge sys_clk_i or negedge sys_rst_n) begin
		if (!sys_rst_n) begin
			rst_sync_r1 <= 1'b1;
			rst_sync_r2 <= 1'b1;
		end else begin
			rst_sync_r1 <= 1'b0;
			rst_sync_r2 <= rst_sync_r1;
		end
	end
	assign sys_clk_rst = rst_sync_r2; // Use this reset for logic clocked by sys_clk_i

	// Original complex reset - kept for reference or potential use where ui_clk domain reset is needed
	// wire	rst = (ui_clk_sync_rst | eb_rst) | (~init_calib_complete);


