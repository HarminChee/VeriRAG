// DFT Fix: Ensure FIFOs use synchronous reset and primary clocks where appropriate.
// Note: Changing rd_clk/wr_clk requires careful CDC analysis for functional correctness.
// This change primarily addresses the DFT requirement of using primary/controllable clocks.

wr_fifo32to256 write_fifo1 (
  .rst					(sys_clk_rst), // Use synchronous reset
  .wr_clk				(WR1_CLK),     // Primary input clock
  .rd_clk				(sys_clk_i),   // DFT Fix: Use primary clock sys_clk_i instead of ui_clk
  .din					(WR1_DATA),
  .wr_en					(WR1),
  .rd_en					(WR1_RDEN), // Driven by logic clocked by sys_clk_i
  .dout					(mDATAIN1),    // Output used by logic clocked by sys_clk_i
  .full					(WR1_FULL),
  .empty					(WR1_EMPTY),
  .rd_data_count		(write_side_fifo_rusedw1), // Output used by logic clocked by sys_clk_i
  .wr_data_count		(WR1_USE)
);
// WR1_RDEN logic depends on app_wdf_rdy (from ui_clk domain) and WR_MASK (from sys_clk_i domain) - Needs CDC
assign	WR1_RDEN = 	app_wdf_wren & app_wdf_rdy & WR_MASK[0]; // Needs CDC for app_wdf_rdy and app_wdf_wren

