module surround_with_regs(
	i_data0,
	o_data0,
	clk
);
	input   [31:0] i_data0;
	output  [31:0] o_data0;
	reg  [31:0] o_data0;
	input clk;
	reg [31:0] i_data0_reg;
	wire [30:0] o_data0_from_mult;
	always @(posedge clk) begin
		i_data0_reg <= i_data0;
		o_data0 <= o_data0_from_mult;
	end
	multiplier_block mult_blk(
		.i_data0(i_data0_reg),
		.o_data0(o_data0_from_mult)
	);
endmodule
module multiplier_block (
    i_data0,
    o_data0
);
  input   [31:0] i_data0;
  output  [31:0]
    o_data0;
  wire [31:0]
    w1,
    w64,
    w65,
    w16,
    w49,
    w16640,
    w16591,
    w6272,
    w22863;
  assign w1 = i_data0;
  assign w16 = w1 << 4;
  assign w16591 = w16640 - w49;
  assign w16640 = w65 << 8;
  assign w22863 = w16591 + w6272;
  assign w49 = w65 - w16;
  assign w6272 = w49 << 7;
  assign w64 = w1 << 6;
  assign w65 = w1 + w64;
  assign o_data0 = w22863;
endmodule 
